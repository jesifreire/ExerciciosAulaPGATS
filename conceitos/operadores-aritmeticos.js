/**
 + - Soma / Adicao
 - - Subtracao
 * - Multiplicacao
 / - Divisao
 % - Resto da divisao
 ** - Exponenciacao
 
 */

console.log(1 + 1); // 2
console.log(10 - 7); // 3
console.log(2 * 2); // 4

console.log(11 / 2); // 5.5
console.log(11 % 2); // 5.5 

console.log(4 ** 4); // 8

console.log(2 + 3 * 4); // 14  Precedência de multiplicação
console.log((2 + 3) * 4); // 20

